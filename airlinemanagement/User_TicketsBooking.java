    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
if(passengerid.getText().equals(""))
        JOptionPane.showMessageDialog(null, "Passenger ID is Mandatory!");

        else if(pname.getText().equals(""))
        JOptionPane.showMessageDialog(null, "Passenger Name is Mandatory!");
        else if(fid.getText().equals(""))
        JOptionPane.showMessageDialog(null, "Flight ID is Mandatory!");
        else if(gender.getText().equals(""))
        JOptionPane.showMessageDialog(null, "Gender Time is Mandatory!");
        else if(pid.getText().equals(""))
        JOptionPane.showMessageDialog(null, "Passport ID is Mandatory!");
        else if(amount.getText().equals(""))
        JOptionPane.showMessageDialog(null, "Amount is Mandatory!");
        else if(nationality.getText().equals(""))
        JOptionPane.showMessageDialog(null, "Nationality is Mandatory!");
        else
        {

     try{
        Class.forName("com.mysql.cj.jdbc.Driver");

        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root", "Hellojavashit69");
        String sql = "insert into ticketbooking values (?,?,?,?,?,?,?)";
        PreparedStatement ptst= con.prepareStatement(sql);
        ptst.setString(1,passengerid.getText());
        ptst.setString(2,pname.getText());
        ptst.setString(3,fid.getText());
        ptst.setString(4,gender.getText());
        ptst.setString(5,pid.getText());
        ptst.setString(6,amount.getText());
        ptst.setString(7,nationality.getText());

        ptst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data inserted Succesfully!");
        con.close();
       }
        catch(Exception e)
        {
        JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_SaveActionPerformed

